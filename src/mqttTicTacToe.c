#define ADDRESS     "tcp://broker.emqx.io:1883"
#define CLIENTID    "emqx_test"
#define TOPIC       "testConnect"
#define PAYLOAD     "Hello World TEST"
#define QOS         1
#define TIMEOUT     10000L

